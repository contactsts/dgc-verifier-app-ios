import Foundation
import UIKit


class UIViewUtils {
    
    static func getDivider() -> UIView {
        let view = UIView()
        view.backgroundColor = UIColor.init(red: 0.9, green: 0.9, blue: 0.9, alpha: 1)
        return view
    }
    
}
